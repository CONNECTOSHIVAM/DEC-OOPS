//Question 2:- write a program to check number is even or odd.

/* Logic:-
       1.even number:- jinte number 2 se full devide hote hai wo number like:- 2,4,6,8,10....
       2.odd number:- jitne number 2 se full devide nahi hote hai wo number like:- 3,5,7,9,11....
 */

public class Question2 {

    public static void main(String[] args) {

        // step 1:- take number to user.
        int num = 0;

        // step 2:- using the conditional statement(if-else), print the even or odd
        // number.
        if (num % 2 == 0) {
            System.out.println("Entered number " + num + " is even number.");
        } else {
            System.out.println("Entered number " + num + " is odd number.");
        }

    }
}
